public class ForDemo {
    

    public static void main(String[] args){
        for (int i = 10; i > 0; i--){
            System.out.println("i = " + i);
        }
        
        String str = "Chattanooga";
        for (int i = 0; i < str.length(); i++){
            System.out.print(str.charAt(i));
        }


    }
}
